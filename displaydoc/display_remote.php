		<h2> Wireless Game Control with Gyroscope and Android </h2>

			<h4>Abstract:</h4>
			Use Android phone and an external gyrosrope to remote contorl a game scene built with Irrlicht.<br/>
<br/>


			<h4>Detail:</h4>
			Use the phone to control the angle and directions of the view, and use the gyroscope to go forward or backward.<br/>
<br/>


			<h4>Parameter:</h4>
			<ul>
				<li><strong>Game Engine:</strong> Irrlicht</li>
				<li><strong>OS:</strong> Ubuntu 14.04 + Android 4.2</li>
				<li><strong>Device:</strong> ThankPad + Android phone + Gyroscope with bluetooth</li>

			</ul>


			<h4>Keywords:</h4>
			<ul>
				<li><strong>C++ </strong>  </li>
				<li><strong>Android </strong>  </li>
				<li><strong>Bluetooth, WiFi</strong> </li>

			</ul>


