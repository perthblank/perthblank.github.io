			<h4>Abstract:</h4>
			A simple structure of particle system using openGL based on C++
		<br/><br/><br/>

			<h4>Details:</h4>
			A basic particle system consists of two parts
			<ul>
				<li>Particle </li>
				<li>Particle Generator </li>
			</ul>
			where the Particle has the attributes including position, velocity, color, size, etc. and the Generator is a controller of a swarm of particles.
			Another component of this system is called Generator Conllector, a higher encapsulation upon the generator. The Collector can control serveral same generators in different position

		
